defmodule Back.App do
  @moduledoc """
  The App context.
  """

  import Ecto.Query, warn: false
  alias Back.Repo

  alias Back.App.User

  @doc """
  Returns the list of users.

  ## Examples

      iex> list_users()
      [%User{}, ...]

  """
  def list_users do
    Repo.all(User)
  end

  @doc """
  Gets a single user.

  Raises `Ecto.NoResultsError` if the User does not exist.

  ## Examples

      iex> get_user!(123)
      %User{}

      iex> get_user!(456)
      ** (Ecto.NoResultsError)

  """
  def get_user!(id), do: Repo.get!(User, id)

  @doc """
  Creates a user.

  ## Examples

      iex> create_user(%{field: value})
      {:ok, %User{}}

      iex> create_user(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_user(attrs \\ %{}) do
    %User{}
    |> User.changeset(attrs)
    |> Repo.insert()
  end

  def login(email, password) do
    query = from u in User, where: u.email == ^email
    user = Repo.one(query)

    if is_nil(user) do
      {:error, "user doesn't exists"}
    else
      with {:ok, %User{} = connectedUser} <-
             Bcrypt.check_pass(user, password, hash_key: :password) do
        with {:ok, token, _claims} <- generate_token(user) do
          {:ok, %{user: connectedUser, token: token}}
        end
      end
    end
  end

  def generate_token(user) do
    signer = Joken.Signer.create("HS256", "kaimesecret")
    Back.Token.generate_and_sign(%{id: user.id, role: user.role}, signer)
  end

  def check_token(token) do
    signer = Joken.Signer.create("HS256", "kaimesecret")
    Back.Token.verify_and_validate(token, signer)
  end

  @doc """
  Updates a user.

  ## Examples

      iex> update_user(user, %{field: new_value})
      {:ok, %User{}}

      iex> update_user(user, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_user(%User{} = user, attrs) do
    user
    |> User.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a user.

  ## Examples

      iex> delete_user(user)
      {:ok, %User{}}

      iex> delete_user(user)
      {:error, %Ecto.Changeset{}}

  """
  def delete_user(%User{} = user) do
    Repo.delete(user)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking user changes.

  ## Examples

      iex> change_user(user)
      %Ecto.Changeset{data: %User{}}

  """
  def change_user(%User{} = user, attrs \\ %{}) do
    User.changeset(user, attrs)
  end

